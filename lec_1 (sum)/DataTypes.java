public class DataTypes {
    public static void main (String args []) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = 105;
    
        System.out.println(price);
        int number = 55;
        System.out.println(number);

        // long
        // double 
        short n = 240;
        System.out.println(n);
    }
}
